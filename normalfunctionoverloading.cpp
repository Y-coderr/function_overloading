#include<iostream>
using namespace std;
int volume(int l)
{
    return(l*l*l);
}
int volume(int l, int b , int h)
{
    return(l*b*h);
}
int volume (double r , double h)
{
    return (3.14*r*r*h);
}
int main()
{
   cout<<"volume of cube:"<<volume(9)<<endl;
   cout<<"volume of cuboid:"<<volume(9,4,6)<<endl;
   cout<<"volume of cylinder:"<<volume(10.2,5.5)<<endl;
    
}
